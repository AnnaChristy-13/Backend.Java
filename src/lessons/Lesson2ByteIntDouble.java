package lessons;

public class Lesson2ByteIntDouble {
    public static void main(String[] args) {

        int x = 20;
        int y = 4;

        System.out.println(x * y);
        System.out.println(x / y);

        double xdouble = 20.8;
        double ydouble = 4.4;

        System.out.println( xdouble / ydouble);
        System.out.print( xdouble + ydouble) ;


    }
}
